export function buildNigeriaLocationLabel(location) {
  if (!location) {
    return "";
  }

  return `${location.city}, ${location.state}`;
}

export function getNigeriaLocationById(id) {
  const locationId = String(id || "").trim();
  return nigeriaLocations.find((location) => location.id === locationId) || null;
}

export function getNigeriaLocationByLabel(label) {
  const normalizedLabel = String(label || "").trim().toLowerCase();

  return (
    nigeriaLocations.find(
      (location) => buildNigeriaLocationLabel(location).toLowerCase() === normalizedLabel
    ) || null
  );
}

export function buildLocationDisplay(location, addressNote = "") {
  const locationLabel = buildNigeriaLocationLabel(location);
  const note = String(addressNote || "").trim();

  if (!note) {
    return locationLabel;
  }

  return `${locationLabel} - ${note}`;
}
